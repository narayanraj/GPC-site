		<h4>Computer Engineering Department </h4>
        
	
	<p align="justify">
	The Computer Engineering Department was established in this Institution in 2006. This Department conducts a 3 year Diploma Course in Computer Engineering, each year spanning two Semesters, with end examinations conducted. The sanctioned intake capacity is 40 students. In addition, 1 student from vocational stream of Higher Secondary and 1 student from ITI are admitted each year.


	<br />
	<br />	
	<u>Course Content</u> <br />
The course has been designed in such a manner that the student gains insight into the Computer Engineering which is a perfect blend of computer software and hardware. Subjects like Electronic Devices and Circuits, Digital Technique, Peripheral and Interfacing, Microprocessor and Programming covers adequate practical design fundamentals and techniques for embedded systems, which is again a need of technological development in the electronics industry.
The course imparts practical training along with necessary theoretical background in Program and Software development using C, C++, and Java etc which are very popular in the Software development industries.	<br />
